package Array;

public class intVaribleSwapping {
	
	public static void main(String[] args) {
		
		int a=50;
		int b=70;
		
		int z;      // swapping integer
		
		z=a;        // z=50
		a=b;        // a=70
		b=z;        // b=50
		
		System.out.println(a);
		System.out.println(b);
		//System.out.println(z);
	}

}
