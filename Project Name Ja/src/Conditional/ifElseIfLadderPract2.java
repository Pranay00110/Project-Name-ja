package Conditional;

public class ifElseIfLadderPract2 {

	public static void main(String[] args) {
		
	int marks = 20;
	
	if(marks >90) {
		System.out.println("10 mark of the student is minus");
	}
		
	else if (marks>80)
	{
		System.out.println("20 mark of the student is minus");
	}
		
	else if (marks <60) {
		System.out.println("40 mark of the student is minus ");
	}
		
	else {
		System.out.println("Student is failed");
	}
	
	}
}
