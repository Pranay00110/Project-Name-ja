package Conditional;

public class switchPract1 {
	
	public static void main(String[] args) {
		
		int No =2;
		
		switch (No) {
		
		case 1 : 
		System.out.println("TATA");
		break;
		
		case 2 :
			System.out.println("MAHINDRA");
			break;
			
		case 3:
			System.out.println("ASHOK LEYLAND");
			break;
			
			default:
				System.out.println("Select no 1 to 3");
		}
	}

}
