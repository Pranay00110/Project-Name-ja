package Operators;

public class Relational {
public static void main(String[] args) {

	byte a = 40 , b = 70 ;
	
	// value of a & b
	
	System.out.println("a value is " + a + " b value is " +b);
	
	//Greater than
	System.out.println(a<b);
	
	//Less than
	System.out.println(a>b);
	
	//Less than equal to
	System.out.println(a>=b);
	
	//Greater than equal to
	System.out.println(a<=b);
	
	
	// Equal to ==
    System.out.println(a==b);
    
     
    //Not Equal to !
    System.out.println(a!=b);
    
    // In Wrong manner equal to
    System.out.println(a=b);
    System.out.println(b=a);
	
	
}
}
