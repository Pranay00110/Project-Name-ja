		byte a=50, b=80;
		
		System.out.println("The value of A is " + a +" The value of B " + b );
		
		System.out.println(a<b);  // smaller than
		
		System.out.println(a>b);  // greater than
		
		System.out.println(a<=b); // smaller than equal to
		
		System.out.println(a>=b); // greater than equal to
		 
		System.out.println(a==b); // equal to
		
		System.out.println(a=b); // equal to in wrong manner    i.e in console big value
		
		
System.out.println("Different");
		
