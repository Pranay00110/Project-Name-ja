package Operators;

public class RelationalPract1 {
	
public static void main(String[] args) {
	
	byte x=20, y=60;
	
	System.out.println("The value of x is "+x+"The value of y is "+y);
	
	System.out.println(x<y);
	
	System.out.println(x>y);
	
	System.out.println(x<=y);
	
	System.out.println(y<=x);
	
	//System.out.println(x=y);
	
	System.out.println(x==y);
	
	System.out.println(!(x==y));
	
	System.out.println(x=y);
}
}
