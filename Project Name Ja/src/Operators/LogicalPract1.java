package Operators;

public class LogicalPract1 {

	public static void main(String[] args) {
		
	
		//AND && operator
		
		System.out.println((10<20) &&(20>10));
		
		System.out.println((40<50)&&(40>60));
		
		
		// OR || operator
		
		System.out.println((10<20)||(30>40));
		
		System.out.println((50<30)||(80>70));
		
		
		// ! Not equal to operator
		
		System.out.println(!(3==3));
		
		System.out.println(!(2==3));
	}
}
