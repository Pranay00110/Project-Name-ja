package Operators;

public class ArithmaticPract {

	public static void main(String[] args) {
		
		
		byte a=50, b=70;
		
		System.out.println("The value of A + B is " +(a+b));                // Addtion
		
		System.out.println("The value of A - B is " +(a-b));                // Substraction
		
		System.out.println("The value of A * B is  " +(a*b));               // Multiplication
		
		System.out.println("The value of A / B "+(a/b));                    // Dividation
		
		System.out.println("The value of A % B "+(a%b));                    // Module
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
